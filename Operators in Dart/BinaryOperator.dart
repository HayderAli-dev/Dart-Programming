void main() {
//The operators that are placed between two operands
  int hisAge = 30;
  int herAge = 17;
  print(hisAge + herAge);
  print(hisAge - herAge);
  print(hisAge * herAge);
  print(hisAge / herAge); //Divide and give answer in double
  print(hisAge ~/ herAge); //Divide and give answer in int
  print(hisAge < herAge); //Check less than condition
  print(hisAge > herAge); //Check greater than condition
  print(hisAge <= herAge); //Check  than and equal to condition
  print(hisAge >= herAge); //Check greater than and equal to condition
}
