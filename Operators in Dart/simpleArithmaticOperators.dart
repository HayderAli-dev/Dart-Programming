void main() {
// + Operator .. it returns num type value
  const age1 = 21;
  const age2 = 23;
  print(age1 + age2);

  // - Operator .. it returns num type value
  print(age1 - age2);

  // * Operator .. it returns num type value
  print(age1 * age2);

  // / Operator .. it returns double type value
  print(age1 / age2);

  // ~/ Operator .. it returns int type value used for division same as above
  print(age1 ~/ age2);
}
