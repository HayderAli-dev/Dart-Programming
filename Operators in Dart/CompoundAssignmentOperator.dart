void main() {
  //Compound Assignment Operators
  int myAge = 10;
  print(myAge = 90); //It will first assign value to variable and then print
  print(myAge += 20); //myAge=myAge+20;
  print(myAge -= 20);
  print(myAge *= 20);
  double age = 1.3;
  print(age /= 50);
  print(myAge ~/= 20);
  print(myAge ^= 20);
}
